package conditionalStatements;

public class SwappingData {

	public static void main(String[] args) 
	{
		int num1=28;
		int num2=47;
		
		System.out.println("Before Swapping Two Numbers is :"+num1+" and "+num2);
		
		int temp=num1;
		num1=num2;
		num2=temp;
		System.out.println("After Swapping Two Numbers is:"+num1+" and "+num2);

	}

}
